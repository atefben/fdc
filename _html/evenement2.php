          <div class="text">
            <p class="text-quote">“…Une leçon de cinéma n’a de sens, à mes yeux, que si elle est pratique, le travail sur le plateau, les prises, diriger l’équipe, les acteurs, commander, fais ceci, fais cela... Sur le plateau, la démocratie, l’égalité n’existent pas, il faut de la rigueur, de l’intérêt, de l’affection, du respect... Je ne crois pas en ces réalisateurs qui, pour faire pleurer, pensent être en droit de gifler une belle fille ou de l’insulter, etc. (on raconte souvent l’histoire de ce réalisateur de la fin du néo-réalisme qui, pour faire pleurer une toute jeune actrice, la frappait sur les jambes avec une cravache tandis qu’il la filmait en gros plan.)… Mais dans la mesure où je ne peux ici apporter qu’un témoignage oral, je dis que la chose la plus précieuse qu’un réalisateur puisse enseigner à ceux qui veulent faire ce métier (un métier, entre autres, extrêmement compliqué et je suis toujours étonné qu’il séduise tant de jeunes) c’est le travail avec les acteurs et les actrices… Parce que ce n’est pas la même chose de diriger un acteur ou une actrice lorsqu’il y a nécessité de convaincre, de “séduire” (et la “séduction” uniquement “artistique” d’une actrice est totalement différente de la “séduction” d’un acteur), voire d’être “séduits”, ce qui ne signifie pas nécessairement être fragiles ou passifs… Tout le monde peut enseigner la technique mais la manière de faire interpréter un personnage que vous avez imaginé par un être humain vivant est un don de la nature. On peut toutefois l’apprendre, en partie, d’un metteur en scène qui ne se soustrait pas au risque d’un échec…
Il est impossible de garantir une jolie fin à toute relation humaine…”</p>
<p>
<p class="paragraph">
Marco Bellocchio, après des études d’art dramatique et de cinéma, réalise en 1965 un premier long métrage remarqué par la critique. Ses oeuvres engagées dénoncent les symboles du conformisme italien : après le film culte <a href="#" title="les poings dans les poches">Les Poings dans les poches </a>( 1966), manifeste d’une jeunesse en révolte, il dénonce la religion dans <a href="#" title='au nom du père'>Au nom du père</a> (1971) ou l’armée dans <a href="#" alt="La marche trimphale">La Marche triomphale </a>(1976). Avec Michel Piccoli et Anouk Aimée, il remporte à Cannes deux prix d’interprétation pour <a href="#" title="Le saut dans le vide">Le Saut dans le vide</a> (1980). 
</p>
<p class="paragraph">

Il réalise aussi des adaptations littéraires, comme Diable au corps qui fait scandale à Cannes en 1986, ou <a href="#" title="La nourrice">La Nourrice</a> (1990) d’après Pirandello. Marco Bellocchio dérange à nouveau le Vatican avec <a href="#" title="Le Sourire de ma mère">Le Sourire de ma mère</a> (Compétition Festival de Cannes - 2002). Il sera le premier à évoquer l’activisme des brigades rouges dans Buongiorno, notte présenté à la Mostra en 2004. En 2009, il présente Vincere en Compétition à Cannes, salué par la critique internationale.</p>
