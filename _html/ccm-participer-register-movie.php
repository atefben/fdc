                        <h4>Courts métrages en Compétition test</h4>
                        <h5>Conditions de présélection et formulaire d'inscription 2016</h5>

                        <p class="info"><strong>IMPORTANT</strong>
                            <br> Cette année, les courts métrages sont directement mis en ligne lors du processus de votre inscription via le téléchargement (cf. spécificités techniques ci-dessous).
                            <br> Tout court métrage envoyé sur un autre support (DVD, BluRay, mini DV,Beta…) ne sera pas visionné.
                            <br> Il n'y a aucun frais à régler pour l'inscription d'un court métrage et votre film ne doit être téléchargé qu'une seule fois.
                            <br>
                            <br> N.B. : Il est possible de présenter une copie de travail du film. En remplissant la fiche d’inscription en ligne, vous devrez préciser s’il s’agit de la version définitive ou d’une version de travail. Le téléchargement d’une copie de travail ne permet pas le téléchargement ultérieur d’une copie définitive.</p>

                        <h5>CRITÈRES</h5>

                        <p>Les courts métrages proposés à la sélection doivent répondre aux critères suivants :</p>

                        <ul>
                            <li>avoir été réalisés au cours des douze mois précédant le Festival de Cannes</li>
                            <li>ne pas avoir été exploités ailleurs que dans leur pays d'origine</li>
                            <li>ne pas avoir été présentés dans une autre manifestation cinématographique internationale. Si le film est passé dans une sélection internationale (compétitive ou non) d’un festival, il n’est pas éligible pour le Festival de Cannes. Une sélection est internationale dès lors qu’elle présente des films de différentes nationalités.</li>
                            <li>ne pas avoir été diffusés sur Internet</li>
                            <li>la durée des films ne doit pas dépasser 15 minutes, génériques inclus.</li>
                        </ul>

                        <h5><br>DURÉE DES FILMS</h5>

                        <p><u><strong>Pour être sélectionnable en Compétition un court métrage doit avoir une durée inférieure à 15 minutes (génériques inclus).</strong></u>
                        </p>

                        <p>Le <em>Festival de Cannes</em> n’accepte pas les films dont la durée est comprise entre 15<sub>e</sub> et 60<sup>e</sup> minutes.</p>

                        <p>Tous les courts métrages (de 1 à 35 minutes) peuvent être inscrits au <strong><a href="http://www.cannescourtmetrage.com/fr/" target="_blank">Short Film Corner</a></strong> : plateforme dédiée aux courts métrages au sein du Festival de Cannes.</p>

                        <p>Ces films peuvent également être proposés à la <a href="http://www.quinzaine-realisateurs.com/" target="_blank"><strong>Quinzaine des Réalisateurs</strong></a> ou à la <a href="http://www.semainedelacritique.com/"><strong>Semaine de la Critique</strong></a> qui sont des sections parallèles au Festival de Cannes.
                            <br> 
                        </p>

                        <h5>DATE LIMITE D'INSCRIPTION</h5>

                        <p>La date limite d’inscription est fixée au <strong>4 mars 2016</strong> (il est impératif qu'à cette date, le formulaire d'inscription ait été rempli et le téléchargement effectué).
                            <br> 
                        </p>

                        <h5>SPECIFICITES TECHNIQUES</h5>

                        <p><strong>Comment télécharger votre film en ligne ?</strong>
                            <br> Nous acceptons la majorité des types de fichiers sources. Si le fichier de votre film est sur votre ordinateur, il est très probable que vous puissiez le télécharger rapidement et simplement.</p>

                        <p>Si votre film est sur DVD, veuillez consulter le manuel "Comment convertir votre film pour l'upload en ligne ?"</p>

                        <p><a href="http://www.festival-cannes.fr/assets/File/WEB%202015/PDF/How_to_prepare_a_film_file_for_the_upload.pdf" target="_blank"><span class="button">Voir le manuel "cOMMENT CONVERTIR VOTRE FILMPOUR L'UPLOAD EN LIGNe"</span></a>
                        </p>

                        <p><strong>Spécifications vidéo :</strong>
                            <br> Formats acceptés :
                            <br> .mp[e]g / .avi / .vob / .mov / .mp4 / .m4v
                            <br> Codecs recommandés :
                            <br> MJPEG
                            <br> Apple ProRes
                            <br> H264 (bitrate si possible) &gt; 2MBit/s et non compressé (très gros fichiers)
                            <br> Résolution :
                            <br> PAL / NTSC (ou SECAM) de 720x400 pixels au format HD (1920x1080 pixels)</p>

                        <p><strong>Spécifications audio :</strong>
                            <br> 48kHz PCM (seulement non compressé)
                            <br> MP3
                            <br> AAC (si possible &gt; 160 kBit/s)</p>

                        <p><strong>Caractéristiques du fichier :</strong>
                            <br> Seuls les fichiers multiplexés seront acceptés (vidéo, audio et sous-titres dans un seul et même fichier). La taille maximum du fichier doit être de 8 GO pour chaque film. Plus la résolution est importante, meilleure sera la qualité de numérisation. Vous pouvez mettre en ligne des films avec une résolution allant jusqu’à 1920x1080 pixels (HD), tout en respectant le critère de taille évoqué plus haut.</p>

                        <p><strong>Sous-titres :</strong>
                            <br> Le Comité de Sélection pourra visionner un film :
                            <br> • SANS sous-titres si la langue originale du film est le français ou l'anglais.
                            <br> • AVEC des sous-titres en français ou en anglais pour les autres langues.
                            <br> Les sous-titres doivent être incrustés dans l’image.</p>

                        <p><strong>Préparation du film :</strong>
                            <br> Assurez-vous que votre film ne comporte pas de barres de couleurs ou de séquence noire avant le début ou après la fin du film. Assurez-vous également que votre film ne soit pas entrelacé (c’est-à-dire avec des lignes horizontales striant l’image, particulièrement visibles lors des séquences avec du mouvement rapide).</p>

                        <p><strong>Configuration logiciels :</strong>
                            <br> Le téléchargement s’effectue en HTML 5 ou via Java (qui peut être téléchargé ici).</p>

                        <p><strong>Renseignements utiles :</strong>
                            <br> Vous trouverez ci-dessus le manuel expliquant comment extraire votre film d’un DVD ou réencoder un fichier vidéo dans un format adapté au téléchargement sur notre site. Outre les logiciels professionnels type Final Cut ou Premiere, les logiciels que nous recommandons (gratuits et simples d’utilisation) sont HandBrake et MPEG Streamclip.</p>

                        <p><strong>Navigateurs et connexion Internet :</strong>
                            <br> Nous vous conseillons d’utiliser Chrome ou Firefox, mais les autres navigateurs doivent fonctionner également. Attention : plus votre fichier sera lourd, plus il vous faudra une connexion Internet de très bonne qualité pour être sûr que votre film soit téléchargé sur notre site sans encombre et sans que cela soit trop long. Si le téléchargement ne fonctionne pas, essayez à nouveau avec un encodage moins lourd de votre film et/ou une connexion plus rapide et plus stable.
                            <br> 
                            <br> 
                        </p>

                        <h5>SI VOTRE FILM EST SÉLECTIONNÉ...</h5>

                        <p>Vous serez prévenu avant la conférence de presse officielle, qui a lieu vers la mi-avril.</p>

                        <p>Vous devrez prévoir une projection du film en DCP (Digital Cinema Package) avec un fichier en JPEG 2000 ou fournir une copie 35mm.</p>

                        <p>Le Département Films vous donnera toutes les indications en temps voulu uniquement si votre film est sélectionné.
                            <br> 
                            <br> Nous éditons chaque année un DVD des films sélectionnés en Compétition officielle. Pour cela, vous devrez nous faire parvenir le matériel suivant :
                            <br>
                            <strong>- une BETA numérique NTSC ou un fichier Apple ProRes HD à 23,976 sur un disque dur et/ou une clé USB et par mail les fichiers STL N19 des sous-titres français et anglais.</strong>
                        </p>
